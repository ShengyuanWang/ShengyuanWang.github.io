#include <bits/stdc++.h>
using namespace std;

void solve() {
    int a;
    cin >> a;
    if (a == 2) {
        cout << "NO" << endl;
        return;
    }
    if (a % 2 == 0) {
        cout << "YES" << endl;
        return;
    } else {
        cout << "NO" << endl;
        return;
    }
}

int main() {
    solve();
}